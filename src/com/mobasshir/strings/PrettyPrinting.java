package com.mobasshir.strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 435.1312f;
        System.out.printf("Formatterd value is %.2f", a);
        System.out.println();
        System.out.printf("Pie: %.3f", Math.PI);
        System.out.println();
        System.out.printf("Hello my name is %s and age is %d", "Mobasshir", 19);
    }
}
